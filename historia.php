                <h1>Biografía</h1>
                <p>Nacido y crecido en un proyecto de vivienda en Carolina, Puerto Rico, hijo de José Gazmey, un
                    afro-puertorriqueño que se desempeñó como vicepresidente de la división puertorriqueña del
                    departamento de A&R Sony Music.Su madre, quien se crio en Milwaukee, es de ascendencia blanca
                    puertorriqueña.
                    <br>
                    <br>
                    Cuando era niño, su padre trabajaba en el estudio con artistas de salsa como Héctor Lavoe y Fania
                    All-Stars. Si bien no se identifica como un fanático del género de dicho género, recuerda que ver a
                    su padre interactuar con estos artistas inspiró un interés en la industria discográfica.13Sin
                    embargo, su padre perdió su trabajo cuando el tenía quince años, lo que supuso una presión económica
                    para la familia y lo llevó a convertirse en lo que la revista Rolling Stone describió como un "hijo
                    de la calle".
                    <br>
                    <br>
                    Idolatraba al rapero estadounidense Tupac Shakur mientras crecía y aspiraba a emular su estilo de
                    vestir, particularmente su énfasis en las joyas.</p>
                <h1>Carrera musical</h1>
                <h1>Inicios</h1>
                <p>
                    Declaró: "Mi música es mi alma hablando, literalmente. Es espiritual. Tiene muchos sentimientos,
                    mucho dolor. Son mis experiencias al crecer en la calle".Comenzó a grabar música a los catorce
                    años y a publicarla en línea a los dieciocho en 2010.Su música obtuvo millones de
                    reproducciones y, finalmente, fue contratado por el rapero estadounidense Rick Ross para la división
                    latina de Maybach Music Group.El éxito de este mixtape lo llevó a conseguir un artículo invitado
                    en el álbum Odisea de Ozuna en 2017.</p>
                <h1>2016-2018: Encarcelamiento y Real hasta la muerte</h1>
                <p>En abril de 2016, fue arrestado en Guaynabo (Puerto Rico), y sentenciado a treinta meses de prisión
                    federal por posesión ilegal de tres armas de fuego.Billboard señaló que el trap latino ganó
                    popularidad mientras estaba en prisión, escribiendo, "Anuel permaneció tras las rejas, mientras que
                    afuera, su música floreció".Después de ser arrestado y encarcelado, grabó su álbum debut
                    mientras estaba en prisión.Hizo esto principalmente grabando su voz por teléfono y aprovechando
                    su estadía obligatoria en un centro de rehabilitación de Miami para completar el álbum.
                    <br>
                    <br>
                    Lanzó su álbum debut Real hasta la muerte el 17 de julio de 2018, el día en que salió de prisión.
                    Rolling Stone lo incluyó en su lista de «Mejores álbumes latinos de 2018», señalando que el artista
                    demuestra «instintos de pop impresionantes» y además comentó: «Mientras Anuel mantiene un pie en el
                    trap, también está mirando hacia la corriente principal del pop latino».Real hasta la muerte
                    alcanzó el puesto 42 en el Billboard 200 y encabezó la lista Billboard Top Latin Albums en los
                    Estados Unidos.El lanzamiento del álbum, junto con la liberación del artista de la prisión,
                    atrajo una atención significativa de los medios; se sorprendió por la respuesta del público y afirmó
                    que «ni siquiera sabía hablar en las entrevistas» después de estar encarcelado por más de dos años.
                    Logró ubicar su primer éxito en el Billboard Hot 100 en agosto de 2018 a través de su colaboración
                    con el rapero estadounidense 6ix9ine en la canción «Bebe».
                    <br>
                    <br>
                    En los seis meses posteriores al lanzamiento de Real Hasta la Muerte, contribuyó con su voz en once
                    sencillos de Billboard Hot Latin Songs, incluido "«Ella quiere beber» con el cantante de bachata
                    dominicano-estadounidense Romeo Santos.También colaboró con Nicki Minaj y Bantu en la canción
                    «Familia» para la banda sonora de Spider-Man: Un nuevo universo, lanzada en diciembre de 2018.En
                    enero de 2019, lanzó el sencillo «Secreto» con la cantante colombiana Karol G, confirmando la
                    relación romántica entre los dos artistas en el video musical que acompaña a la canción, el cual
                    alcanzó el número 68 en el Billboard Hot 100 y el número cinco en las listas de Hot Latin Songs de
                    Estados Unidos.Se inspiró en el momento en que estaban saliendo pero aún no habían hablado
                    públicamente de su relación.</p>
