                <h1>2021-presente: Los dioses y Las leyendas nunca mueren</h1>
                <p>El 22 de enero de 2021, junto con Ozuna lanzó su álbum colaborativo Los dioses que incluyó doce
                    pistas, y ocupó el primer puesto en los Top Latin Albums y Latin Rhythm Albums de Billboard, el
                    Apple Music Daily de Apple Music en varios países, y el Top Debut Albums de Spotify.En julio de
                    ese mismo año, apareció en el remix del sencillo «Mr. Jones», del álbum Faith del difunto rapero
                    estadounidense Pop Smoke.En agosto, lanzó dos canciones «Los de siempre» con Chris Jedi y «23
                    preguntas», dedicadas a su expareja Karol G.En septiembre, colaboró para Jhay Cortez en el
                    tema «Ley seca», parte del álbum de Cortez, Timelezz. El videoclip superó el medio millón de
                    visualizaciones en menos de 24 horas en YouTube.
                    <br>
                    <br>
                    En octubre de 2021, lanzó «Dictadura», como el primer sencillo de su próximo álbum Las leyendas
                    nunca mueren. Fue producido por Súbelo Neo y Machael, y escrito por Mora y AA. Cuenta, además, con
                    un videoclip dirigido por él mismo.El 3 de noviembre, participó en el «Bzrp Music Sessions,
                    Vol. 46», junto al productor argentino Bizarrap.El sencillo ocupó el séptimo puesto en el
                    Argentina Hot 100 de Billboard.
                    <br>
                    <br>
                    El 29 de noviembre de ese mismo año, lanzó su tercer álbum en solitario Las leyendas nunca
                    mueren.El álbum ocupó el primer lugar en los Top Latin Albums y Latin Rhythm Albums de
                    Billboard, así como también en Spotify y Apple Music.</p>
