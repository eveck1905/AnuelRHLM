                <h1>Historia de Anuel</h1>
                <p>Emmanuel Gazmey Santiago (Carolina, Puerto Rico; 25 de noviembre de 1992), más conocido por su
                    nombre artístico
                    Anuel AA, es un rapero, cantante y compositor puertorriqueño de trap latino y reguetón.
                    2021-presente
                    <br>
                    <br>
                    Criado en Carolina (Puerto Rico), comenzó a grabar música a los catorce años y comenzó a
                    publicarla en línea
                    cuatro años después en 2010, antes de finalmente firmar con la división latina del grupo musical
                    Maybach Music
                    Group del rapero estadounidense Rick Ross.
                    <br>
                    <br>
                    En 2016, su carrera quedó en suspenso el mismo año. a ser un convicto por una sentencia de
                    prisión de treinta
                    meses por posesión ilegal de armas de fuego en Puerto Rico.Grabó la totalidad de su álbum debut
                    mientras
                    estaba encarcelado, tiempo durante el cual su género musical ganó popularidad.El 17 de julio de
                    2018, fecha
                    en que fue liberado de prisión, lanzó su álbum debut titulado Real hasta la muerte..En los
                    próximos seis
                    meses, apareció en Billboard Hot Latin Songs, consolidando su posición como uno de los mejores
                    artistas
                    latinos.En agosto de 2019, lanzó la canción «China», una colaboración con Daddy Yankee, Karol G,
                    Ozuna y J
                    Balvin.Desde entonces, lanzó otras como «Me gusta» con Shakira, y su segundo álbum, Emmanuel,
                    que fue
                    lanzado el 29 de mayo de 2020.También en noviembre del año 2021 lanzó su tercer álbum de estudio
                    titulado
                    Las leyendas nunca mueren, el cual ocupó el primer lugar en los Top Latin Albums y Latin Rhythm
                    Albums de
                    Billboard, así como también en Spotify y Apple Music</p>
