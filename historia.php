                <h1>2019-2020: «China» y Emmanuel</h1>
                <p>En julio de 2019, lanzó «China», una colaboración con Daddy Yankee, Karol G, Ozuna y J Balvin. La
                    canción muestra en gran medida el sencillo de Shaggy de 2000, «It Wasn't Me».«China» debutó en
                    el número dos en la lista Hot Latin Songs de Billboard y encabezó las listas Latin Digital Songs y
                    Latin Streaming Songs con 1,000 descargas vendidas y 14.1 millones de transmisiones, mientras que
                    el video que lo acompaña se incluyó en la lista de Rolling Stone, «10 mejores videos de música
                    latina de julio».Luego, apareció en el sencillo «Whine Up» del álbum Íntimo de Nicky Jam,
                    lanzado el 1 de noviembre de 2019.
                    <br>  
                    <br>  
                    En enero de 2020, colaboró con Shakira en la canción «Me gusta», que interpola la canción de 1992 de
                    la banda de reggae jamaicana Inner Circle, «Sweat (A La La La La Long)».Rolling Stone llamó a la
                    canción «un número de rock para un amante pausado con un delicado toque de dembow».Después,
                    apareció en el álbum YHLQMDLG de Bad Bunny en la canción «Está cabrón ser yo».
                    <br> 
                    <br> 
                    El 3 de abril, publicó el sencillo «3 de abril» que conmemora el día en que el artista fue
                    arrestado.La canción refleja su problemática juventud y los desafíos que enfrentó mientras
                    estaba encarcelado. Suzy Exposito de Rolling Stone calificó la pista como una «conmemoración
                    aleccionadora».También en abril, lanzó el sencillo y el video de «Follow» con Karol G, filmado
                    en Miami.
                    <br>
                    <br>
                    El 27 de mayo de 2020, anunció y reveló la lista de canciones de su segundo álbum, Emmanuel, que fue
                    lanzado el 29 de mayo.Originalmente había planeado lanzar el álbum en abril, pero pospuso la
                    fecha debido a la pandemia.El día del estreno del álbum, lo promovió con el lanzamiento de tres
                    yates de lujo frente a la costa del vecindario Isle of Normandy de Miami.Cada yate tenía el
                    nombre Emmanuel escrito en el costado y tocaba su música a un volumen alto. El rapero explicó su
                    estrategia promocional diciendo: "Todos hacen su fiesta de lanzamiento en un club. ¿Y con el
                    coronavirus? ¡Yo no iría al club ahora mismo!".El álbum doble de 22 pistas cuenta con
                    colaboraciones de Bad Bunny, Enrique Iglesias, Travis Barker, Tego Calderón, Karol G, Lil Wayne,
                    Farruko, Yandel y Ñengo Flow.Con respecto al título del álbum, el artista explicó: Es mi
                    nombre y significa "Dios con nosotros. Quería que el álbum tuviera esa buena vibra. Es mi vida,
                    convertida en música.
                    <br>
                    <br>
                    En noviembre de 2020, indicó en una publicación de Instagram que se retiraba de la industria de la
                    música.El 19 de noviembre, lanzó una nueva canción, «Me contagié 2», en la que habla sobre la
                    depresión, indica infelicidad con sus relaciones y expresa angustia por el hecho de que su carrera a
                    menudo lo aleja de su hijo pequeño, quien le ha pedido que se retire. Cierra la canción con el
                    enunciado "En estos Grammy, me retiro".Los comentarios han dejado a sus fanáticos en un estado
                    de confusión sobre sus planes futuros, ya que «parece que aún tiene que hacer un anuncio más formal
                    de retiro».</p>
