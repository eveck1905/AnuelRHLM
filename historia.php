                <h1>Estilo e influencias musicales</h1>
                <p>Según el sitio XXL, es considerado uno de los padres fundadores de la música trap latina.Es
                    conocido por su firma "BRRR", que pretende imitar el sonido de un arma de fuego automática.Sus
                    letras a menudo hablan sobre el sexo, el crimen y la vida en las calles;Paul Simpson de Allmusic
                    las describió como "demasiado atrevidas para ser reproducidas en la radio" y destacó que su éxito se
                    debe a los servicios de transmisión y la presencia muy activa del artista en las redes sociales.
                    Por otro lado, Gary Suárez de la revista Vice vio su álbum de 2018 Real hasta la muerte como una
                    colección de "relucientes trap bangers" y "reguetón que complacen al público".A pesar de su
                    "imagen de chico malo", Suárez escribió que las canciones con temas de relación «Culpables» y
                    «Secreto» representan "un ablandamiento de su endurecida personalidad de trapero".
                    <br>
                    <br>
                    Asimismo, se destaca por el uso de la palabra "bebecita" en canciones y en las redes sociales que se
                    ha asociado con el género del reguetón, así como con su relación con Karol G.
                    <br>
                    <br>
                    Tras su introducción al hip-hop que se produjo al escuchar al rapero Tupac Shakur, sus canciones
                    frecuentemente rinden homenaje a artistas estadounidenses y latinos que lo influenciaron en su
                    juventud; los ejemplos incluyen «China», que muestra tanto «It Wasn't Me» de Shaggy y «Ella me
                    levantó» de Daddy Yankee, y «Delincuente», que interpola «Bandoleros» de Don Omar y Tego
                    Calderón.Está influenciado y ha colaborado con los artistas estadounidenses de hip-hop como Meek
                    Mill y Gucci Mane, quienes al igual que él, ganaron notoriedad por cumplir condena en prisión.Al
                    describir el impacto de Meek Mill en su música, explicó: "Escucho lo que la gente dice en las
                    canciones, no solo las melodías. Con los raperos tienes que escuchar lo que dicen. Y, Meek está
                    hablando cosas de verdad. Por eso admíralo".En una entrevista con Vice, expresó su admiración
                    por la música del artista de bachata Zacarías Ferreira, el rapero dominicano de dembow, El Alfa y el
                    cantautor inglés Ed Sheeran.También ha mostrado interés en colaborar con Post Malone y Billie
                    Eilish.</p>

                <h1>Vida personal</h1>
                <h1>Arresto</h1>
                <p>El 3 de abril de 2016, fue puesto bajo arresto junto a otras personas cuando según las autoridades,
                    salió de la discoteca «Tabaco & Ron Lounge San Juan» en Santurce, Puerto Rico.Durante el control
                    policial, se le incautaron tres pistolas, una de ellas robada; una Glock modelo 23 calibre .40; otra
                    modelo 30 calibre .45 y una tercera modelo 19 calibre 9 mm; además de nueve cargadores y ciento
                    cincuenta y dos municiones, en un automóvil Honda Accord del año 2007.
                    <br>
                    <br>
                    Sus fanáticos convocaron una manifestación en su apoyo a las afueras de la cárcel federal en
                    Guaynabo el 15 de mayo de 2016,portando pancartas y cigarrillos en su alusión, al momento de ser
                    detenido por las autoridades.De la misma manera, existió una campaña para su liberación llamada
                    Free Anuel.
                    <br>
                    <br>
                    El 19 de julio de 2016, fue sentenciado a treinta meses de prisión por posesión ilícita de armas de
                    fuego.A pesar de su privación de libertad no tuvo obstáculos para el lanzamiento de nuevas
                    canciones; por ejemplo, la nueva versión del sencillo «Ayer» llamado «Ayer 2», el cual contó con la
                    colaboración de J Balvin, Nicky Jam y Cosculluela; para la grabación del disco, tuvo que cantar a
                    través de una llamada telefónica, situación que fue bien recibida por sus fanáticos tal como se
                    aprecia en YouTube y en las otras plataformas en las que subió la canción.[cita requerida]
                    <br>
                    <br>
                    Por otra parte, recibió un disco platino certificado por la Recording Industry Association of
                    America (RIAA) gracias a las altas ventas de «Ayer» grabado bajo la producción de DJ Nelson. También
                    se mostró agradecido con toda la gente que lo siguió apoyando y que a pesar de su situación no lo
                    han olvidado, afirmando:
                    <br>
                    <br>
                    Gracias a ustedes tengo el privilegio de recibir mi primer disco de platino estando en prisión
                    todavía. Este premio se lo dedico a mi hijo y a ustedes que escuchan mi música y son Real hasta la
                    muerte.
                    Además, grabó un álbum completo llamado Real hasta la muerte en colaboración con otros artistas;
                    múltiples sencillos como «Me contagié» en colaboración con Kendo Kaponi y «Ceniza en cenicero»</p>

                <h1>Relaciones sentimentales</h1>
                <p>Es padre de hijo llamado Pablo Anuel con su excónyuge, Astrid Cuevas.Luego conoció a la cantante
                    colombiana Karol G en agosto de 2018 en el estudio del video musical de su canción «Culpables», un
                    mes después de su liberación de prisión.En enero de 2019, confirmaron su relación, y 25 de
                    abril del mismo año, ésta llegó a los Premios Billboard de la Música Latina con un anillo de
                    diamantes "masivo", confirmando el compromiso de la pareja. En marzo de 2021, se informó que se
                    separaban después de casi un año de compromiso.
                    <br>
                    <br>
                    A continuación, en 2022, comenzó una relación con la cantante dominicana Yailin La Más Viral, con
                    quien se casó en junio del mismo año.El 9 de febrero de 2023, ocho meses después del matrimonio,
                    anunció su separación y divorcio de ésta.</p>
                <img data-src="assets/images/Karol_G_Telemedellín.jpg" alt="">
                <h1>Controversias</h1>
                <p>El 15 de septiembre de 2018, lanzó una tiraera llamada «Intocable», dirigida al también rapero
                    Cosculluela, la cual fue ampliamente criticada debido a su blasfemia y comentarios sobre la
                    homosexualidad y los pacientes con VIH/sida.En la canción, se refiere a la modelo y presentadora
                    de televisión La Taína como un "cerdo" por su estado de VIH positivo.Debido a la reacción del
                    público, su concierto en la sede del Coliseo de Puerto Rico, programado para el 12 de octubre de ese
                    año, fue cancelado por su equipo de producción y productor principal Paco López; luego se disculpó
                    por el contenido de la canción y explicó: Es el peor error de mi carrera. No necesito esto y
                    pido disculpas a todas las personas a las que ofendí. A los homosexuales, a las personas con sida, a
                    La Taina [la modelo] y a los que perdieron todo después. Huracán María ... Mis fanáticos merecen que
                    yo sea una mejor persona, y pido disculpas a todos en Puerto Rico.
                    <br>
                    <br>
                    A principios de abril de 2019, discutió con la cantante Ivy Queen en Instagram después de que éste
                    cuestionara cómo es que ella todavía podría ser considerada la «reina del reguetón», argumentando
                    que no había tenido una canción exitosa en más de siete años, y además cuestionó si Karol G, su
                    entonces pareja, debería adoptar el término.Los seguidores en esa red social especularon sobre
                    por qué hizo los comentarios, mientras tanto Ivy Queen respondió con comentarios sobre de dónde
                    venía y cómo fue una pionera, allanando el camino para que otras mujeres la siguieran.</p>
